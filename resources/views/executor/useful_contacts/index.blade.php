            <h2>📌 Additional Useful Contacts for Executors</h2>

            <h3>📈 1. Deceased Assets & Investments</h3>
            <ul>
            <li><a href="https://www.shareview.co.uk" target="_blank">Equiniti Shareview</a> – Manage
              shareholdings and dividends.</li>
            <li><a href="https://www.computershare.com" target="_blank">Computershare</a> – Share registrar
              services.</li>
            <li><a href="https://www.linkgroup.eu" target="_blank">Link Group</a> – Share registry and
              investment services.</li>
            <li><a href="https://www.nsandi.com" target="_blank">National Savings & Investments (NS&I)</a> –
              Cash in Premium Bonds and other holdings.</li>
            <li><a href="https://www.bsa.org.uk" target="_blank">Building Societies Association</a> – Find local
              and regional building societies.</li>
            </ul>

            <h3>💼 2. Employment & Pensions</h3>
            <ul>
            <li><a href="https://www.gov.uk/find-pension-contact-details" target="_blank">Pension Tracing
              Service</a> – Locate workplace or personal pensions.</li>
            <li>Contact the deceased’s employer – For final salary, holiday pay, or death-in-service benefits.
            </li>
            <li><a href="https://www.gov.uk/government/organisations/uk-armed-forces-pension-scheme"
              target="_blank">Armed Forces Pension Scheme</a> – For military pension claims.</li>
            </ul>

            <h3>🪪 3. Membership & Subscription Services</h3>
            <ul>
            <li>Contact relevant professional bodies – e.g. RICS, ICAEW, trade unions (may offer death
              benefits).</li>
            <li>Contact charities or organisations – If the deceased left legacy donations or memberships.</li>
            <li>Cancel subscription services – Netflix, Amazon Prime, gyms, magazines, clubs.</li>
            </ul>

            <h3>🚗 4. Vehicles & Transport</h3>
            <ul>
            <li>Contact motor insurer – Cancel or transfer policy.</li>
            <li>Contact vehicle finance company – Settle outstanding agreements.</li>
            <li><a href="https://www.gov.uk/vehicle-tax-refund" target="_blank">DVLA Vehicle Tax Refund</a> –
              Apply for refund of remaining road tax.</li>
            </ul>
            <h3>🏡 5. Property-Related</h3>
            <ul>
            <li>Contact mortgage provider – Settle outstanding loan or arrange transfer.</li>
            <li>Contact leasehold freeholder/management company – Update records for flats or leasehold
              properties.</li>
            <li>Contact tenants – If the deceased was a landlord.</li>
            </ul>

            <h3>🌍 6. International</h3>
            <ul>
            <li>Contact relevant foreign embassy/consulate – For nationals of another country.</li>
            <li>Contact foreign probate authority – If assets are located abroad.</li>
            </ul>

            <h3>💙 7. Specialist Bereavement Support</h3>
            <ul>
            <li><a href="https://www.samaritans.org" target="_blank">Samaritans</a> – Emotional support for
              anyone struggling (UK: 116 123).</li>
            <li><a href="https://www.widowedandyoung.org.uk" target="_blank">WAY Widowed and Young</a> – Support
              for people widowed under age 50.</li>
            <li><a href="https://uksobs.org" target="_blank">Survivors of Bereavement by Suicide (SOBS)</a> –
              Specialist support for suicide bereavement.</li>
            </ul>

            <h3>💻 8. Digital Asset Management</h3>
