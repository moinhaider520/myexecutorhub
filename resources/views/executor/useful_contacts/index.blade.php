            <li><a
              href="https://support.microsoft.com/en-gb/account-billing/how-to-close-a-microsoft-account-1d1a7d3f-31f3-3e13-3f21-8438b9f2f70f"
              target="_blank">Microsoft Account Closure Request</a> – Close or transfer Microsoft accounts.
            </li>
