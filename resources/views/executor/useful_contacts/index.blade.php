            <h2>🔗 Useful Links After a Death</h2>

            <h3>⚖️ 1. Legal & Probate</h3>
            <ul>
            <li><a href="https://www.gov.uk/applying-for-probate" target="_blank">HM Courts & Tribunals Service
              – Probate</a> – Apply for probate online or find forms and guidance.</li>
            <li><a href="https://www.nationalwillregister.co.uk" target="_blank">Certainty National Will
              Register</a> – Search for a registered will.</li>
            <li><a href="https://www.mylostaccount.org.uk" target="_blank">My Lost Account</a> – Search for lost
              bank, building society, and NS&I accounts.</li>
            <li><a href="https://solicitors.lawsociety.org.uk" target="_blank">Law Society – Find a
              Solicitor</a> – Locate a solicitor for probate or disputes.</li>
            </ul>

            <h3>🏛️ 2. Government Notifications</h3>
            <ul>
            <li><a href="https://www.gov.uk/tell-us-once" target="_blank">Tell Us Once</a> – Notify government
              departments in one step.</li>
            <li><a href="https://www.gov.uk/government/organisations/hm-revenue-customs/contact/bereavement"
              target="_blank">HMRC Bereavement Service</a> – Help with tax, inheritance tax, and refunds.</li>
            <li><a href="https://www.gov.uk/death-benefits" target="_blank">DWP Bereavement Service</a> – Check
              benefits and pensions.</li>
            <li><a href="https://www.gov.uk/cancel-driving-licence" target="_blank">DVLA – Cancel Driving
              Licence</a> – Return or cancel a deceased person’s licence.</li>
            <li><a href="https://www.gov.uk/cancel-passport" target="_blank">Passport Office – Cancel
              Passport</a> – Prevent misuse and update records.</li>
            </ul>

            <h3>💰 3. Financial & Insurance</h3>
            <ul>
            <li><a href="https://www.ukfinance.org.uk/policy-and-guidance/reports-publications/deceased-account"
              target="_blank">UK Finance – Deceased Account Service</a> – Notify multiple banks at once.</li>
            <li><a
              href="https://www.abi.org.uk/products-and-issues/choosing-the-right-insurance/life-insurance/making-a-claim"
              target="_blank">Association of British Insurers (ABI)</a> – How to claim on life insurance.</li>
            <li><a href="https://www.gov.uk/find-pension-contact-details" target="_blank">Pension Tracing
              Service</a> – Find any pensions belonging to the deceased.</li>
            </ul>
            <h3>🏠 4. Property & Utilities</h3>
            <ul>
            <li><a href="https://www.gov.uk/search-property-information-land-registry" target="_blank">Land
              Registry</a> – Transfer or update property ownership.</li>
            <li><a href="https://www.royalmail.com/personal/receiving-mail/redirection" target="_blank">Royal
              Mail Redirection</a> – Redirect post to the executor or next of kin.</li>
            <li><a href="https://www.gov.uk/find-local-council" target="_blank">Find Your Local Council</a> –
              Update council tax and rates.</li>
            <li><a href="https://www.ofgem.gov.uk" target="_blank">Ofgem – Notify Energy Supplier</a> – Advice
              on switching or closing accounts.</li>
            </ul>

            <h3>🩺 5. Health & Care</h3>
            <ul>
            <li><a href="https://www.nhs.uk/nhs-services/gps/how-to-register-with-a-gp-practice/"
              target="_blank">NHS – Inform GP</a> – Remove from patient lists and cancel appointments.</li>
            <li><a href="https://www.cqc.org.uk" target="_blank">Care Quality Commission (CQC)</a> – Raise
              concerns about care homes or providers.</li>
            </ul>

            <h3>🌸 6. Memorial, Funeral & Bereavement Support</h3>
            <ul>
            <li><a href="https://www.nafd.org.uk" target="_blank">National Association of Funeral Directors</a>
              – Find a regulated funeral director.</li>
            <li><a href="https://www.cruse.org.uk" target="_blank">Cruse Bereavement Support</a> – Free grief
              counselling and resources.</li>
            <li><a href="https://www.thegoodgrieftrust.org" target="_blank">The Good Grief Trust</a> –
              Signposting to local and national bereavement services.</li>
            </ul>

            <h3>💻 7. Digital & Online Accounts</h3>
            <ul>
            <li><a href="https://digitallegacyassociation.org" target="_blank">Digital Legacy Association</a> –
              Guide to managing online accounts after death.</li>
            <li><a href="https://www.facebook.com/help/1506822589577997" target="_blank">Facebook
              Memorialisation</a> – Turn a profile into a memorial page.</li>
            <li><a href="https://support.google.com/accounts/troubleshooter/6357590" target="_blank">Google
              Account Closure Request</a> – Close or transfer Google accounts.</li>
            <li><a href="https://www.linkedin.com/help/linkedin/ask/ts-rdc" target="_blank">LinkedIn – Deceased
              Profile Removal</a> – Request account removal.</li>
